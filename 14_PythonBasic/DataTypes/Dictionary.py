# Keys and values can be of different types, but keys must be unique
# Values can be added or modified
oDics={"K1":"Val1", 2:"val2", "K3":32}
print(oDics)
print(oDics["K1"])
# print the keys
print(oDics.keys())
# print the values
print(oDics.values())
# print all the keys and values
print(oDics.items())
# Find the length of a dictionary
print(len(oDics))